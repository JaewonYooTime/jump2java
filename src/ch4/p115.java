package ch4;

public class p115 {
    public static void main(String[] args){
        int x = 3;
        int y = 2;
        System.out.println(String.format("x > y == %b",x > y) );
        System.out.println(String.format("x < y == %b",x < y) );
        System.out.println(String.format("x == y == %b",x == y) );
        System.out.println(String.format("x != y == %b",x != y) );
    }
}
